package kuis;

public class Animal {
    private String nama;
    private int umur;

    // Setter
    public void setNama(String nama) {
        this.nama = nama;
    };
    public void setUmur(int umur) {
        this.umur = umur;
    };
    // Getter
    public String getNama() {
        return this.nama;
    };
    public int getUmur() {
        return this.umur;
    };
}
